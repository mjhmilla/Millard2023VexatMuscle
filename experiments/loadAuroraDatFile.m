function auroraData = loadAuroraDatFile(fullFilePath, ...
                                        numberOfHeaderLines,...
                                        rowOfColumnLabels,...
                                        rowOfUnitLabels)

auroraData.data = readmatrix(fullFilePath,...
                               'FileType','text',...
                               'NumHeaderLines',numberOfHeaderLines,...
                               'Delimiter',' ',...
                               'ConsecutiveDelimitersRule','join');

auroraData.data = auroraData.data(:,2:end);

auroraData.columnNames = ...
    readmatrix(fullFilePath,...
       'FileType','text',...
       'Delimiter','  ',...
       'ConsecutiveDelimitersRule','join',...
       'OutputType','string',...
       'Range',...
       [rowOfColumnLabels,1,rowOfColumnLabels,size(auroraData.data,2)+1]);

auroraData.columnNames = auroraData.columnNames(:,2:end);

auroraData.columnUnits = ...
    readmatrix(fullFilePath,...
       'FileType','text',...
       'Delimiter','  ',...
       'ConsecutiveDelimitersRule','join',...
       'OutputType','string',...
       'Range',[rowOfUnitLabels,1,rowOfUnitLabels,size(auroraData.data,2)+1]);

auroraData.columnUnits = auroraData.columnUnits(:,2:end);
