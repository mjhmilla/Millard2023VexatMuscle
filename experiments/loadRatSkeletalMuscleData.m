function [ratSkeletalMuscleData, ratSkeletalMuscleMetaData] = ...
                loadRatSkeletalMuscleData(projectFolders)

for i=1:1:3
    ratSkeletalMuscleData(i).activeForceLengthData=[];
    ratSkeletalMuscleData(i).passiveForceLengthData=[];
    ratSkeletalMuscleData(i).activeLengtheningData=[];
    ratSkeletalMuscleData(i).activeLengtheningBDMData=[];
    
end

%
% Tomalka et al. 2017
%

fileActiveForceLength_TRSS2017 = ...
    fullfile(projectFolders.experiments_TRSS2017,...
             'data','fig_TomalkaRodeSchumacherSiebert2017_Fig2_fl.csv');

filePassiveForceLength_TRSS2017 = ...
    fullfile(projectFolders.experiments_TRSS2017,...
             'data','fig_TomalkaRodeSchumacherSiebert2017_Fig2_fpe.csv');

fileActiveLengthening_TRSS2017 = ...
    fullfile(projectFolders.experiments_TRSS2017,...
             'data','fig_TomalkaRodeSchumacherSiebert2017_Fig2A_al.csv');

fileActiveLengtheningBDM_TRSS2017 = ...
    fullfile(projectFolders.experiments_TRSS2017,...
             'data','fig_TomalkaRodeSchumacherSiebert2017_Fig3A.csv');

ratSkeletalMuscleData(1).activeForceLengthData =...
        loadDigitizedData( ...
        fileActiveForceLength_TRSS2017,...
        'Length (um)','Norm. Force (f/fo)',...
        {'Exp.'},...
         'Rat fibril (EDL) $$f^{L}$$');

ratSkeletalMuscleData(1).passiveForceLengthData =...
        loadDigitizedData( ...
        filePassiveForceLength_TRSS2017,...
        'Length (um)','Norm. Force (f/fo)',...
        {'Exp.'},...
         'Rat fibril (EDL) $$f^{PE}$$'); 

ratSkeletalMuscleData(1).activeLengtheningData =...
        loadDigitizedData( ...
        fileActiveLengthening_TRSS2017,...
        'Length (um)','Norm. Force (f/fo)',...
        {'Exp. 0.70 $$\ell^{M}_{o}$$',...
         'Exp. 0.85 $$\ell^{M}_{o}$$',...
         'Exp. 1.0 $$\ell^{M}_{o}$$'},...
         'Rat fibril (EDL) active lengthening'); 

ratSkeletalMuscleData(1).activeLengtheningBDMData =...
        loadDigitizedData( ...
        fileActiveLengtheningBDM_TRSS2017,...
        'Length (um)','Norm. Force (f/fo)',...
        {'Exp. 0.85 $$\ell^{M}_{o}$$',...
         'Exp. 0.85 $$\ell^{M}_{o}$$+2mM BDM',...
         'Exp. 0.85 $$\ell^{M}_{o}$$+5mM BDM',...
         'Exp. 0.85 $$\ell^{M}_{o}$$+10mM BDM'},...
         'Rat fibril (EDL) active lengthening (BDM): $$0.85-1.30 \ell^{M}_{o}$$');

ratSkeletalMuscleMetaData.index_TRSS2017=1;

%
% Zuurbier et al.
%

fileActiveForceLength_ZHGL1995 = ...
    fullfile(projectFolders.experiments_ZHGL1995,...
    'data','ZuurbierHeslingaGrootLaarse1995.csv'     );
        
ratSkeletalMuscleData(2).activeForceLengthData =...
            loadDigitizedData( ...
            fileActiveForceLength_ZHGL1995,...
            'Length (um)','Norm. Force (f/fo)',...
            {'ZHGL1995 Exp: $$\mu$$',...
             'ZHGL1995 Exp: $$\mu+\sigma$$',...
             'ZHGL1995 Exp: $$\mu-\sigma$$',...
             'Model (GM)','Model (EDL)'},...
             'Rat fibril (GM + EDL) $$f^{L}$$');

ratSkeletalMuscleMetaData.index_ZHGL1995=2;

%
% Stephenson & Williams 
%

fileActiveForceLength_SW1982 = ...
